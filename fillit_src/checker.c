#include "fillit.h"

int		check_file(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	if (i < 1)
		return (0);
	i--;
	while (str[i] && str[i] == '\n' && (i == 0 || str[i - 1] == '\n'))
		str[i--] = 0;
	//if (i )
	return (1);
}